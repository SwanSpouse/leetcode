package sort_and_search;

import utils.Print;

public class SortArrayByParityII {
    public int[] sortArrayByParityII(int[] A) {
        int i = 0, j = 1, n = A.length;
        while (i < n && j < n) {
            while (i < n && A[i] % 2 == 0) {
                i += 2;
            }
            while (j < n && A[j] % 2 == 1) {
                j += 2;
            }
            if (i < n && j < n) {
                swap(A, i, j);
            }
        }
        return A;
    }

    private void swap(int[] A, int i, int j) {
        int temp = A[i];
        A[i] = A[j];
        A[j] = temp;
    }

    public static void main(String[] args) {
        Print.printArr(new SortArrayByParityII().sortArrayByParityII(new int[]{2, 4, 1, 1, 1, 3, 4, 3, 2, 0}));
    }
}
